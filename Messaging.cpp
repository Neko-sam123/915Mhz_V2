#include "Messaging.h"

void Message::init() {
  node.init();
}

void Message::select_addr(uint32_t destAddr){
  this->destAddr = destAddr;
	RREQinterval = millis();
  if(node.findRoute(destAddr,true) == -1){
    node.sendRREQ(destAddr);
    while(millis() - RREQinterval < 3000 && node.route_established == false){                      //// may loop d2
			currentT = millis();
			draw_animation(loadingallArray,loadingallArray_LEN, "Sending RREQ");
      node.receivePacket();
			node.updateRadio();
      node.update_Expiry_time();
    }
  }
  loadMessagesFromSD(destAddr);
}

void Message::sendMessage() {
  handleKeyboard();

  if (aodv.hasPayload(destAddr)) {
    
    strncpy(message_payload, aodv.getLastPayload(destAddr), sizeof(message_payload) - 1);
    message_payload[sizeof(message_payload) - 1] = '\0';
    Serial.print("Message: ");
    Serial.println(message_payload);

    handlePayloadReceive(message_payload, destAddr);
    aodv.clearLastPayload(destAddr);  // Clear after processing
  }


  if (millis() - lastBlink >= blinkInterval) {
    blinkState = !blinkState;
    lastBlink = millis();
    drawChat();
  }
}

void Message::handleKeyboard() {
  char c = getKey();

  if (c) {
    if (c == 13 && inputLength > 0) {
      node.sendDataMessage(currentInput,destAddr);
        unsigned long sendingT = millis();

        while(!node.message_sent && (millis() - sendingT < 5000)){ //// loop to
          currentT = millis();
          node.receivePacket();
          node.update_Expiry_time();
					node.updateRadio();
          draw_animation(loadingallArray,loadingallArray_LEN, "Sending Message");
          //loading effect
        }

			if (node.message_sent == true) {
					// Message sent successfully
					String msg = String("> You: ") + currentInput;
					addMessage(msg.c_str());
					saveMessageToSD(msg.c_str(), destAddr);      

					// Clear input only on success
					inputLength = 0;
					currentInput[0] = '\0';
			} else {
					// Message failed â€” keep input text so user can retry
					Serial.println("ACK timeout. Message not confirmed sent.");
			}

			// Reset flag
			node.message_sent = false;

    } else if ((c == 8 || c == 127) && inputLength > 0) {
      inputLength--;
      currentInput[inputLength] = '\0';
    } else if (c == 181 && scrollOffset > 0) {
      scrollOffset--;
    } else if (c == 182 && scrollOffset + VISIBLE_MESSAGES < MAX_MESSAGES) {
      scrollOffset++;
    } else if (inputLength < MAX_INPUT_LENGTH && c >= 32 && c <= 126) {
      currentInput[inputLength++] = c;
      currentInput[inputLength] = '\0';
    } else if (c == 27 ){ 
      //node.saveRoutingTable();
      interface_UI_disabler = false;
      node.route_established = false; 
			node.saveNodeStateToNVS();
			
			// clear input when esc is pressed
			inputLength = 0;
			currentInput[0] = '\0';
      return;
    }
    drawChat();
  }
}


void Message::handlePayloadReceive(const char* payload, uint32_t destAddr) {
  if (payload == nullptr || strlen(payload) == 0) return;

  String formatted = "> " + String(destAddr, HEX) + ": " + payload;
  
  addMessage(formatted.c_str());
  saveMessageToSD(formatted.c_str(), destAddr);
  drawChat();
}




char Message::getKey() {
  Wire.requestFrom(KB_ADDR, 1);
  if (Wire.available()) {
    char c = Wire.read();
    if (c != 0 && c != 255) return c;
  }
  return 0;
}



void Message::addMessage(const char* msg) {
  for (int i = 0; i < MAX_MESSAGES - 1; i++) {
    strncpy(messages[i], messages[i + 1], MAX_MSG_LENGTH);
    messages[i][MAX_MSG_LENGTH] = '\0';
  }
  strncpy(messages[MAX_MESSAGES - 1], msg, MAX_MSG_LENGTH);
  messages[MAX_MESSAGES - 1][MAX_MSG_LENGTH] = '\0';
  scrollOffset = (MAX_MESSAGES > VISIBLE_MESSAGES) ? MAX_MESSAGES - VISIBLE_MESSAGES : 0;
}

void Message::drawChat() {
  display.clearDisplay();
  display.setCursor(0, 7);
  
  display.print("To: 0x");
  display.print(destAddr, HEX);  // d2 lalagay kung saan node

  if(node.findRoute(destAddr ,true) != -1){
  display.setCursor(98, 7);
  display.print("Active"); // kung active yung node or hindi
  }else{
  display.setCursor(90, 7);
  display.print("Inactive");
  }

  display.drawLine(0, 10, 128, 10, SSD1306_WHITE);
  int y = 18;
  for (int i = scrollOffset; i < scrollOffset + VISIBLE_MESSAGES && i < MAX_MESSAGES; i++) {
    display.setCursor(0, y);
    display.print(messages[i]);
    y += 19;
  }
  display.drawLine(0, 49, 128, 49, SSD1306_WHITE);
  display.setCursor(0, SCREEN_HEIGHT - 7);
  display.print("> ");
  display.print(currentInput);
  if (blinkState) display.print("_");
  display.setCursor(108, SCREEN_HEIGHT);
  display.print(inputLength);
  display.print("/");
  display.print("32");
  display.display();
}


void Message::saveMessageToSD(const char* msg, uint32_t destAddr) {
  // Ensure folder exists
  if (!SD.exists("/logs")) {
    if (!SD.mkdir("/logs")) {
      Serial.println("Failed to create /logs folder");
      return;
    }
  }

  // Convert address to filename
  char addrStr[9];
  sprintf(addrStr, "%08X", destAddr);  // e.g., "000000A1"

  char filename[64];
  snprintf(filename, sizeof(filename), "/logs/node_%s.txt", addrStr);

  File file = SD.open(filename, FILE_APPEND);
  if (file) {
    file.println(msg);
    file.flush();  // Ensure data is written
    //Serial.println(file.size());
    file.close();
  } else {
    Serial.print("Failed to open ");
    Serial.print(filename);
    Serial.println(" for writing");
  }
}

void Message::loadMessagesFromSD(uint32_t destAddr) {
  // Clear message buffer
  for (int i = 0; i < MAX_MESSAGES; i++) {
    messages[i][0] = '\0';
  }
  scrollOffset = 0;

  // Construct filename
  char addrStr[9];
  sprintf(addrStr, "%08X", destAddr);  // e.g., "000000A1"

  char filename[64];
  snprintf(filename, sizeof(filename), "/logs/node_%s.txt", addrStr);

  Serial.print("Loading file: ");
  Serial.println(filename);

  if (!SD.exists(filename)) {
    Serial.println("File does not exist!");
    return;
  }

  File file = SD.open(filename);
  if (file) {
    char line[MAX_MSG_LENGTH + 1];

    while (file.available()) {
      size_t len = file.readBytesUntil('\n', line, MAX_MSG_LENGTH);
      line[len] = '\0';

      addMessage(line);
    }
    file.close();
  } else {
    Serial.print("Failed to open ");
    Serial.print(filename);
    Serial.println("for reading");
  }
}





void Message::draw_animation(const unsigned char** frames,const int frame_Num, const char* text){
  static  unsigned long draw_messageT = 0;
  static int index = 0;

	if (index < frame_Num && currentT - draw_messageT > 35){
		display.fillRect(0, 0, 128, 64, SSD1306_BLACK);
		display.drawBitmap(32, 0, frames[index], 64, 64, 1);
		display.drawRect(0, 0, 128, 64, SSD1306_WHITE);
		centerText(text,42,64,32);
		display.display();
		index++;
    if(index >= frame_Num){
      index = 0;
    }
		draw_messageT = currentT;
	}
	
}


void Message::centerText(const char* text, int y, int width, int startX) {
    int16_t x1, y1;
    uint16_t w, h;
    display.getTextBounds(text, 0, 0, &x1, &y1, &w, &h); // get width of text

    int x = startX + (width - w) / 2;  // center text inside box

    display.setCursor(x, y);
    display.println(text);
}







