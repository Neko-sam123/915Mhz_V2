void UInterface::centerText(const char* text, int y, int width, int startX) {
    int16_t x1, y1;
    uint16_t w, h;
    display.getTextBounds(text, 0, 0, &x1, &y1, &w, &h); // get width of text

    int x = startX + (width - w) / 2;  // center text inside box

    display.setCursor(x, y);
    display.println(text);
}




void UInterface::draw_animation(const unsigned char** frames,const int frame_Num){

	if (index < frame_Num && currentT - draw_messageT > 35){
		display.clearDisplay();
		display.drawBitmap(0, 5, frames[index], 42, 42, 1);
		index++;
		draw_messageT = currentT;
	}

	if (index >= frame_Num){
		message_clear = true;
		gps_clear = true;
		settings_clear = true;
	}
}



void UInterface::drawUpArrow(int x, int y) {
  display.drawPixel(x + 2, y, SSD1306_WHITE);   //   ▓▓▓▓▓
  display.drawPixel(x + 3, y, SSD1306_WHITE);   //   ▓▓▓▓▓
  
  display.drawLine(x + 1, y + 1, x + 4, y + 1, SSD1306_WHITE);
  display.drawLine(x,     y + 2, x + 5, y + 2, SSD1306_WHITE);
  
  display.drawLine(x + 2, y + 3, x + 3, y + 3, SSD1306_WHITE);
}

void UInterface::drawDownArrow(int x, int y) {
  display.drawLine(x + 2, y, x + 3, y, SSD1306_WHITE);
  display.drawLine(x,     y + 1, x + 5, y + 1, SSD1306_WHITE);
  display.drawLine(x + 1, y + 2, x + 4, y + 2, SSD1306_WHITE);
  display.drawPixel(x + 2, y + 3, SSD1306_WHITE);
  display.drawPixel(x + 3, y + 3, SSD1306_WHITE);
}

void UInterface::drawUpArrowRec(){	
	display.fillRect(0, 0, 42, 4 , SSD1306_WHITE);
	display.display();
}

void UInterface::drawDownArrowRec(){
	display.fillRect(0, 60, 42, 4 , SSD1306_WHITE);
	display.display();
}


void UInterface::arrow_blinking(int x ,int y, int dulo){
	if (currentT - lastBlink >= blinkInterval) {
    blinkState = !blinkState;
    lastBlink = currentT;	
  }

		if (blinkState) {
			display.setCursor(x, y);
			display.print(">");
			display.setCursor(dulo - x, y);
			display.print("<");
	} else {
			display.setCursor(x, y);
			display.print(" ");
			display.setCursor(dulo - x, y);
			display.print(" ");
	}
}




void UInterface::drawBatteryIcon13px(int x, int y, float voltage) {
  // Outer body: 23x13 pixels
  display.drawRect(x, y, 23, 13, WHITE);
  // Positive terminal (2x5)
  display.fillRect(x + 23, y + 4, 2, 5, WHITE);

  // Fill level based on voltage
  float level = (voltage - 3.0) / (4.2 - 3.0);
  if (level < 0) level = 0;
  if (level > 1) level = 1;
  int fillWidth = (int)(level * 21);  // max width inside

  // Fill inside
  display.fillRect(x + 1, y + 1, fillWidth, 11, WHITE);
}















