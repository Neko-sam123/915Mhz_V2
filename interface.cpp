void UInterface::m5_stack(){
			Wire.requestFrom(KB_ADDR, 1); 
	if (Wire.available() && currentT - m5stackT >= 15) {
		char c = Wire.read();

		if (c == 181) {
			if (!interface_navigation_disabler && interface_navigation > 0) {
				drawUpArrowRec();
				interface_navigation--;
				index = 0;
				gps_clear = false;
				message_clear = false;
				settings_clear = false;
			}
			else if (!selection_disabler && selection_value > 0) {
				selection_value--;
			}
		}

		if (c == 182) {
			if (!interface_navigation_disabler && interface_navigation < 2) {
				drawDownArrowRec();
				interface_navigation++;
				index = 0;
				gps_clear = false;
				message_clear = false;
				settings_clear = false;
			}
			else if (!selection_disabler && selection_value < (sizeof(item_buffer) / sizeof(item_buffer[0]) - 1)) {
				selection_value++;
			}
		}

		if (c == 13) {
			if (!interface_navigation_disabler && interface_navigation == 0) {
				selection_disabler = false;
				interface_navigation_disabler = true;
			} else {
				if(interface_navigation == 0){
				interface_UI_disabler = true;
				message.select_addr(Address[selection_value]);
        aodv.clearLastPayload(Address[selection_value]);
				display.clearDisplay();
				display.display();
				}
			}
		}

		if (c == 27) {
			interface_navigation_disabler = false;
			selection_disabler = true;
		}

		m5stackT = currentT;	
	}

}

